#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int	i;
	int	j;

	i = 0;
	while (s1[i] != s2[i])
	{
		if (s1[i] == s2[i])
		{
			while (s1[i + j] == s2[i + j])
			{
				if (s1[i + j] != s2[i + j])
					return (0);
				if (s1[i + j] == '\0')
				{
					return ((char *)s1);
				}
				j++;
			}
			j = 0;
		}
		i++;
	}
	return (NULL);
}
